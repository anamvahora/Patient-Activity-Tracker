    private void btn_StartInterviewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_StartInterviewActionPerformed
    // 1. Get ID
        int pid = Integer.parseInt(lbl_PatientID.getText().replaceAll("[^0-9]", ""));

        // 2. Run the interview
        java.util.Map<String, String> interviewResults = InterviewUtils.runMedicalHistoryInterview(pid);

        if (interviewResults != null && !interviewResults.isEmpty()) {

            // --- THIS PART HANDLES THE TXT FILE SAVING ---

            // A. Build the Question/Answer string for the file
            StringBuilder logContent = new StringBuilder();
            for (java.util.Map.Entry<String, String> entry : interviewResults.entrySet()) {
                logContent.append("Question/Field: ").append(entry.getKey())
                          .append(" -> Answer: ").append(entry.getValue())
                          .append("\n");
            }

            // B. Call the Log Class to create the .txt file
            // This is where the 'magic' happens for Requirement 1.c
            String pName = "Patient #" + pid; // Use your name label here if you have one
            logUtils.writeInterviewLog(pid, pName, "GeneralMedicalHistory", logContent.toString());

            // ----------------------------------------------

            // C. Update the GUI text boxes (so you can see them on screen)
            updateGUIFromMap(interviewResults); 

            setFormMode(true); // Unlock the form
            JOptionPane.showMessageDialog(this, "Interview Logged to Text File and Form Updated!");
        }
    }//GEN-LAST:event_btn_StartInterviewActionPerformed

