    private void loadMedicalHistoryData(int pid) {
    try (Connection conn = dbUtils.ConnectToMySqlDB("root", "")) {
        // Use your medical utility class to fetch the record
        ResultSet rs = medicalHistoryDBUtils.getGeneralMedicalHistory(conn, pid);
        
    if (rs != null && rs.next()) {
        tb_Tobacco.setText(rs.getString("Tobacco"));
        tb_TobaccoQty.setText(rs.getString("TobaccoQuantity"));
        tb_TobaccoDur.setText(rs.getString("Tobaccoduraton")); // Note the SQL typo "duraton"

        tb_Alcohol.setText(rs.getString("Alcohol"));
        tb_AlcoholQty.setText(rs.getString("AlcoholQuantity"));
        tb_AlcoholDur.setText(rs.getString("Alcoholduration"));

        tb_Drug.setText(rs.getString("Drug"));
        tb_DrugType.setText(rs.getString("DrugType"));
        tb_DrugDur.setText(rs.getString("Drugduration")); // Rename this component soon!

        tb_BloodType.setText(rs.getString("BloodType"));
        tb_RH.setText(rs.getString("Rh"));


            JOptionPane.showMessageDialog(this, "Medical History for Patient #" + pid + " loaded.");
        } else {
            JOptionPane.showMessageDialog(this, "No medical history found for Patient ID: " + pid);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Load Error: " + e.getMessage());
    }
}
    
